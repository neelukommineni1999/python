3.Explain when normalization is useful.
Normalization is useful when we want to store data in a clean way without repeating it. 
It is used in systems where data changes often, 
like adding, updating, or deleting records.

4.Explain when denormalization is useful.
Denormalization is useful when we want to get results faster and make reporting easier. 
It is used in systems where we mostly read data, like dashboards and reports.