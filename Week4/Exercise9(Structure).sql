1.Identify facts and dimensions for an e-commerce dataset.
Facts (what we measure):
These are numbers related to business activity:
    Number of orders
    Quantity sold
    Sales amount
    Discount given
    Profit
Dimensions (what we describe):
These are details used to understand the data:
   Customer details (name, city, segment)
   Product details (name, category, brand)
   Date details (day, month, year)
   Region details (country, state, city)


