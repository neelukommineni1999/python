4.Classify examples as transaction fact or snapshot fact.
Transaction Fact (happens as an event)
These are things that happen one by one in real time:
   every order placed
   every product sold
   every payment made
Example:
When order #1002 is placed, that is one transaction.

Snapshot Fact (shows status at a time)
These show how things look at a specific time:
   stock in the warehouse today
   total sales for a month
   bank balance at the end of the day
Example:
Stock = 1002 units on Monday is a snapshot.

