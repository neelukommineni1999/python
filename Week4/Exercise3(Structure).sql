1.Identify relationship type for Customer and Order.
Relationship type: One-to-Many
One customer can place many orders
One order belongs to only one customer

2.Identify relationship type for Product and Category.
Relationship type: One-to-Many
One category can have many products
One product belongs to only one category

3.Identify relationship type for Doctor and Patient in a hospital system.
Relationship type: Many-to-Many
One doctor can treat many patients
One patient can visit many doctors

4.Explain why relationship direction matters.
Relationship direction matters because it tells us how two tables are connected.
It helps us decide:
which table should store the foreign key
how we should join tables to get correct data
how we can write queries for reports properly
If we don’t set the relationship correctly, we may connect the wrong data and get wrong results in reports.
